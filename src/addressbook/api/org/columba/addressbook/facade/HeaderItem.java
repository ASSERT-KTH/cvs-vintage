package org.columba.addressbook.facade;

/**
 * Convenience class implementation of IHeaderItem. This implementation class can be used by clients
 * of the addressbook facade if desired, or can be implemented differently, if necessary.
 */
public class HeaderItem implements IHeaderItem {

	private String id;

	private String name;

	private String description;

	private boolean contact;

	public HeaderItem() {
		
	}
	
	public HeaderItem(boolean contact) {
		this.contact = contact;
	}

	public HeaderItem(String id, boolean contact) {
		if (id == null)
			throw new IllegalArgumentException("id == null");

		this.id = id;
		this.contact = contact;
	}

	/**
	 * @param id
	 *            contact unique id as generated by contact store
	 * @param name
	 *            displayname
	 * @param description
	 *            description, can be used as tooltip
	 * @param contact
	 *            true, if a contact item. False, otherwise.
	 */
	public HeaderItem(String id, String name, String description,
			boolean contact) {
		this(id, contact);
		
		if (name == null)
			throw new IllegalArgumentException("name == null");
		if (description == null)
			throw new IllegalArgumentException("description == null");

		this.name = name;
		this.description = description;
	}

	public String getId() {
		return id;
	}

	/* (non-Javadoc)
	 * @see org.columba.addressbook.facade.IHeaderItem#setId(java.lang.String)
	 */
	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public String getDescription() {
		return description;
	}

	public boolean isContact() {
		return contact;
	}

	public void setName(String name) {
		this.name = name;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public Object clone() {
		IHeaderItem item = null;
		try {
			item = (IHeaderItem)super.clone();
		} catch (CloneNotSupportedException e) {
			// This should never happen since superclass is Object
			e.printStackTrace();
		}
		return item;
	}
}
