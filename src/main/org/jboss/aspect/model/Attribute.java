package org.jboss.aspect.model;

public class Attribute
{
   String name;
   String value;
   
   
   public String getName()
   {
      return name;
   }

   public String getValue()
   {
      return value;
   }

   public void setName(String name)
   {
      this.name = name;
   }

   public void setValue(String value)
   {
      this.value = value;
   }

}
